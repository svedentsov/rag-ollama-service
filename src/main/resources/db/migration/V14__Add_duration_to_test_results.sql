-- Добавляем недостающую колонку для хранения времени выполнения
-- каждого отдельного тест-кейса.
-- `IF NOT EXISTS` делает миграцию безопасной для повторного применения.
-- `DEFAULT 0` устанавливает значение по умолчанию для существующих строк,
-- а `NOT NULL` обеспечивает целостность данных для новых.
ALTER TABLE test_case_run_results
ADD COLUMN IF NOT EXISTS duration_ms BIGINT NOT NULL DEFAULT 0;