package com.example.ragollama.entity;

/**
 * Перечисление для ролей участников в диалоге.
 * <p>
 * В соответствии со спецификацией OpenAI и Spring AI, основными ролями являются:
 * <ul>
 *   <li><b>USER</b>: Сообщение от конечного пользователя.</li>
 *   <li><b>ASSISTANT</b>: Ответ, сгенерированный языковой моделью.</li>
 *   <li><b>SYSTEM</b>: Инструкции высокого уровня, задающие поведение модели (в данном проекте не используется для хранения, но важно для понимания концепции).</li>
 * </ul>
 */
public enum MessageRole {
    /**
     * Сообщение от пользователя.
     */
    USER,
    /**
     * Сообщение от ассистента (AI модели).
     */
    ASSISTANT
}
