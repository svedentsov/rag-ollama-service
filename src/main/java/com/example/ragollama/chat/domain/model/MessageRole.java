package com.example.ragollama.chat.domain.model;

/**
 * Перечисление, определяющее роли участников в диалоге.
 * В соответствии с общепринятой практикой (например, OpenAI API) и Spring AI, выделяются следующие основные роли:
 * <ul>
 *   <li><b>USER</b>: Сообщение, отправленное конечным пользователем.</li>
 *   <li><b>ASSISTANT</b>: Ответ, сгенерированный языковой моделью (AI).</li>
 *   <li><b>SYSTEM</b>: (Не используется в данной реализации для хранения)
 *   Инструкции высокого уровня, задающие поведение и "личность" модели.</li>
 * </ul>
 */
public enum MessageRole {
    /**
     * Сообщение от пользователя.
     */
    USER,
    /**
     * Сообщение от ассистента (AI-модели).
     */
    ASSISTANT
}
