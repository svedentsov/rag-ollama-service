package com.example.ragollama.ingestion.domain.model;

/**
 * Перечисление, определяющее возможные статусы задачи на обработку документа.
 * <p>
 * Определяет жизненный цикл задачи от момента ее создания до завершения
 * или сбоя, обеспечивая прозрачность и возможность отслеживания.
 */
public enum JobStatus {
    /**
     * Задача создана и ожидает в очереди на обработку.
     */
    PENDING,
    /**
     * Задача захвачена воркером и находится в процессе выполнения.
     */
    PROCESSING,
    /**
     * Задача успешно обработана и завершена.
     */
    COMPLETED,
    /**
     * В процессе обработки задачи произошла ошибка.
     */
    FAILED
}
