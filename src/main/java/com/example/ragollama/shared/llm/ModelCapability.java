package com.example.ragollama.shared.llm;

/**
 * Перечисление, определяющее абстрактные "возможности" (capabilities)
 * языковых моделей, доступных в системе.
 * <p>Этот подход позволяет бизнес-логике запрашивать модель, подходящую для
 * конкретной задачи (например, быструю для классификации, мощную для генерации),
 * не зная ее конкретного имени (например, 'llama3' или 'phi3'). Маппинг
 * возможностей на реальные имена моделей осуществляется в
 * {@link LlmRouterService} и настраивается в {@code application.yml}.
 */
public enum ModelCapability {
    /**
     * Самая быстрая модель для простых задач, где формат вывода не критичен.
     * Например, генерация гипотетических документов (HyDE).
     */
    FASTEST,
    /**
     * Быстрая модель, но с улучшенной способностью следовать инструкциям и формату.
     * Идеально для классификации, маршрутизации, извлечения JSON.
     */
    FAST_RELIABLE,
    /**
     * Сбалансированная модель для RAG и чата. Обеспечивает хороший компромисс
     * между скоростью, качеством и способностью следовать сложным инструкциям.
     */
    BALANCED,
    /**
     * Мультимодальная модель, способная анализировать изображения.
     */
    VISION
}
