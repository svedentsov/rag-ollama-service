package com.example.ragollama.shared.llm;

/**
 * Перечисление, определяющее абстрактные "уровни" возможностей языковых моделей.
 * <p>
 * Позволяет бизнес-логике запрашивать модель по ее назначению (например, "быстрая для классификации"),
 * а не по конкретному имени (например, "llama3"), что делает код более гибким и поддерживаемым.
 */
public enum ModelCapability {
    /**
     * Быстрая и легковесная модель.
     * Идеально подходит для задач классификации, извлечения сущностей,
     * маршрутизации и других, где скорость важнее глубины анализа.
     */
    FAST,

    /**
     * Сбалансированная модель.
     * Основная "рабочая лошадка" для большинства RAG-задач и чатов.
     * Предлагает хороший компромисс между качеством, скоростью и стоимостью.
     */
    BALANCED,

    /**
     * Мультимодальная модель с возможностью анализа изображений.
     * Используется для задач визуального регрессионного тестирования и
     * анализа скриншотов.
     */
    VISION
}
