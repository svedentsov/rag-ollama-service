package com.example.ragollama.agent;

import java.util.List;

/**
 * Определяет контракт для самодостаточного, переиспользуемого конвейера AI-агентов.
 * <p>
 * Каждая реализация этого интерфейса представляет собой одну "Стратегию" или
 * бизнес-процесс, который может быть выполнен {@link AgentOrchestratorService}.
 * Этот подход соответствует Принципу Открытости/Закрытости, позволяя добавлять
 * новые конвейеры в систему без модификации существующего кода оркестратора.
 */
public interface AgentPipeline {

    /**
     * Возвращает уникальное, машиночитаемое имя конвейера.
     *
     * @return Имя конвейера (например, "bug-reproduction-pipeline").
     */
    String getName();

    /**
     * Возвращает упорядоченный список агентов, составляющих этот конвейер.
     *
     * @return Неизменяемый список {@link QaAgent}.
     */
    List<QaAgent> getAgents();
}
