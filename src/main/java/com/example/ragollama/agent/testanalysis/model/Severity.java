package com.example.ragollama.agent.testanalysis.model;

import io.swagger.v3.oas.annotations.media.Schema;

/**
 * Перечисление, определяющее серьезность (критичность) элемента
 * тестового технического долга.
 */
@Schema(description = "Серьезность элемента тестового техдолга")
public enum Severity {
    /**
     * Критическая проблема, требующая немедленного внимания.
     */
    CRITICAL,
    /**
     * Серьезная проблема, которую следует исправить в ближайшее время.
     */
    HIGH,
    /**
     * Проблема средней серьезности, может быть исправлена в плановом порядке.
     */
    MEDIUM,
    /**
     * Незначительная проблема или рекомендация по улучшению.
     */
    LOW
}
