package com.example.ragollama.agent.domain;

/**
 * Перечисление, определяющее возможные намерения (intent) пользователя,
 * которые распознает Router Agent.
 */
public enum QueryIntent {
    /**
     * Запрос является частью обычного разговора (small talk, chitchat).
     * Не требует обращения к базе знаний.
     */
    CHITCHAT,

    /**
     * Вопрос, для ответа на который требуется извлечение информации
     * из векторной базы данных (RAG).
     */
    RAG_QUERY,

    /**
     * Явная инструкция сгенерировать фрагмент кода.
     */
    CODE_GENERATION,

    /**
     * Не удалось определить намерение, используется как fallback.
     */
    UNKNOWN
}
