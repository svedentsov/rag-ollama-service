package com.example.ragollama.agent.routing;

/**
 * Перечисление, определяющее возможные намерения (intent) пользователя,
 * которые распознает Router Agent.
 * <p>
 * Каждое намерение соответствует определенному конвейеру обработки
 * или специализированному сервису в системе. В эту версию добавлено
 * намерение для запросов к OpenAPI.
 */
public enum QueryIntent {
    /**
     * Запрос является частью обычного разговора (small talk, chitchat).
     * Не требует обращения к базе знаний.
     */
    CHITCHAT,

    /**
     * Вопрос, для ответа на который требуется извлечение информации
     * из векторной базы данных (RAG).
     */
    RAG_QUERY,

    /**
     * Явная инструкция сгенерировать фрагмент кода.
     */
    CODE_GENERATION,

    /**
     * Запрос на анализ описания бага на предмет дубликатов и улучшений.
     */
    BUG_ANALYSIS,

    /**
     * Запрос на создание краткого содержания (summary) предоставленного текста.
     */
    SUMMARIZATION,

    /**
     * Запрос на семантический анализ OpenAPI спецификации.
     */
    OPENAPI_QUERY,

    /**
     * Не удалось определить намерение, используется как fallback.
     */
    UNKNOWN
}
